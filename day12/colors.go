func ToColor(c uint8) [3]int {
	return gradient[c-'a']
}

func EmbedChar(c uint8) string {
	if c < 'a' || c > 'z' {
		return fmt.Sprintf("\033[1;48;2;255;0;0m%c \033[0m", c)
	}
	color := ToColor(c)
	s := fmt.Sprintf("\033[48;2;%d;%d;%dm%c \033[0m", color[0], color[1], color[2], c)
	return s
}
